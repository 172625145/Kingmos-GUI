// *****************************************************************
//声明：static BOOL __VertialSolidLine( _LPLINEDATA lpLineData, 
//										int x0, 
//										int y0, 
//										int yDir, 
//										unsigned int uLen )
//参数：
//	IN lpLineData - _LINEDATA结构指针，包含画线需要的数据
//	IN x0 - 起点 x 坐标
//	IN y0 - 起点 y 坐标
//	IN yDir - 方向（当 < 0 , 由下向上）
//	IN uLen - 点数
//返回值：
//	返回TRUE
//功能描述：
//	用纯色画垂直线
//引用: 
//	被 line.h 使用
// *****************************************************************
static BOOL __VertialSolidLine( _LPLINEDATA lpLineData, int x0, int y0, int yDir, UINT uLen )
{
    register LPBYTE lpDest;
    register int widthBytes;
	register UINT sum; 
    register BYTE color;
	//扫描行宽度(字节数)
    widthBytes = lpLineData->lpDestImage->bmWidthBytes;
	//目标地址
	lpDest = lpLineData->lpDestImage->bmBits + y0 * widthBytes + x0;
    color = (BYTE)lpLineData->color;
    
    sum = uLen;
	//正向或反向
	if( yDir < 0 )
        widthBytes = -widthBytes;	//反向
	//根据操作模式做不同的处理
    if( lpLineData->rop == R2_COPYPEN )
    {	//拷贝
        while( sum-- )
        {
            *lpDest = color;
            lpDest += widthBytes;
        }
    }
    else if( lpLineData->rop == R2_NOT )
    {	//非
        while( sum-- )
        {
            *lpDest = ~*lpDest;
            lpDest += widthBytes;
        }
    }
    else if( lpLineData->rop == R2_XORPEN )
    {	//异或
        while( sum-- )
        {
            *lpDest = *lpDest ^ color;
            lpDest += widthBytes;
        }
    }
    return TRUE;
}

