// *****************************************************************
//声明：static BOOL __ScanSolidLine( _LPLINEDATA lpLineData, 
//										int x0, 
//										int y0, 
//										int xDir, 
//										unsigned int uLen )
//参数：
//	IN lpLineData - _LINEDATA结构指针，包含画线需要的数据
//	IN x0 - 起点 x 坐标
//	IN y0 - 起点 y 坐标
//	IN xDir - 方向（当 < 0 , 由右向左）
//	IN uLen - 点数
//返回值：
//	返回TRUE
//功能描述：
//	用纯色画水平线
//引用: 
//	被 line.h 使用
// *****************************************************************
static BOOL __ScanSolidLine( _LPLINEDATA lpLineData, int x0, int y0, int xDir, UINT uLen )
{
    register LPBYTE lpDestStart, lpDestEnd;
    register BYTE color;
	//得到开始地址
    lpDestStart = lpLineData->lpDestImage->bmBits + y0 * lpLineData->lpDestImage->bmWidthBytes + x0;
	if( xDir > 0 )
    {	//正向
		lpDestEnd = lpDestStart + uLen;		
    }
    else
    {	//反向
		lpDestEnd = lpDestStart - uLen;
    }
	
    color = (BYTE)lpLineData->color;
    //根据操作模式做不同的处理
	switch( lpLineData->rop )
    {
    case R2_COPYPEN:
        for( ; lpDestStart != lpDestEnd; lpDestStart += xDir )
            *lpDestStart = color;
        break;
    case R2_XORPEN:
        for( ; lpDestStart != lpDestEnd; lpDestStart += xDir )
            *lpDestStart ^= color;
        break;
    case R2_NOT:
        for( ; lpDestStart != lpDestEnd; lpDestStart += xDir )
            *lpDestStart = ~*lpDestStart;
        break;
    }
    return TRUE;
}

