// *****************************************************************
//声明：static COLORREF _PutPixel( _LPPIXELDATA lpPixelData )
//参数：
//	IN lpPixelData - _PIXELDATA结构指针，包含写点需要的数据
//返回值：
//	该点对应的颜色值
//功能描述：
//	在对应的xy点输出颜色值，并返回之前的值
//引用: 
//	
// *****************************************************************
static COLORREF _PutPixel( _LPPIXELDATA lpPixelData )
{
    LPBYTE lpVideoAdr;
    COLORREF c;
	UINT rop;
	//是否需要真实输出 ？
	if( (rop = lpPixelData->rop) != R2_NOP )
	{	//需要输出
		//得到x 位置/地址
		lpVideoAdr = (LPBYTE)(lpPixelData->lpDestImage->bmBits + lpPixelData->y * lpPixelData->lpDestImage->bmWidthBytes) + lpPixelData->x;
		//得到当前/返回的颜色值
		c = *lpVideoAdr;
		//根据操作模式做相关动作
		switch( rop )
		{
		case R2_COPYPEN:		//拷贝到目标
			*lpVideoAdr = (BYTE)lpPixelData->color;
			break;
		case R2_XORPEN:			//异或
			*lpVideoAdr ^= (BYTE)lpPixelData->color;
			break;
		case R2_NOT:			//非
			*lpVideoAdr = ~*lpVideoAdr;
			break;
		case R2_BLACK:			//黑
			*lpVideoAdr = 0;
			break;
		case R2_WHITE:			//白
			*lpVideoAdr = 0xff;
			break;
		case R2_MASKPEN:   // dest = dest-pixel-color and pen-color
			*lpVideoAdr &= (BYTE)lpPixelData->color;
			break;
		case R2_MERGEPEN:  // dest = dest-pixel-color or pen-color
			*lpVideoAdr |= (BYTE)lpPixelData->color;
			break;
		case R2_MERGENOTPEN:
			*lpVideoAdr |= ~(BYTE)lpPixelData->color;
			break;
		case R2_NOTCOPYPEN:
			*lpVideoAdr = ~(BYTE)lpPixelData->color;
			break;
		case R2_NOTXORPEN:
			*lpVideoAdr = ~( *lpVideoAdr ^ (BYTE)lpPixelData->color );
			break;
		}
	}
	else
		c = -1;
    return c;
}

