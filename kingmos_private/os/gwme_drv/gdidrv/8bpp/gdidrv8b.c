// *****************************************************************
//声明：static COLORREF _RealizeColor( COLORREF color, LPCDWORD lpcdwPal, UINT uPalNumber, UINT uPalFormat )
//参数：
//	IN color - RGB 颜色值
//返回值：
//	设备相关的颜色值
//功能描述：
//	由RGB颜色值得到设备相关的颜色值
//引用: 
//	
// *****************************************************************
static COLORREF _RealizeColor( COLORREF color, LPCDWORD lpcdwPal, UINT uPalNumber, UINT uPalFormat )
{
	if( lpcdwPal )
	{
		return _Gdi_RealizeColor( color, lpcdwPal, uPalNumber, uPalFormat );
	}
	else
	{
		const PALETTEENTRY * lp = _rgbIdentity;
		const BYTE * lpsrc, * lpdst;
		LONG lMinValue, lDiff, d1, d2, d3;
		int like, i;
		
		//初始化误差为最大的值
		lMinValue = MAXLONG;//255 + 255 + 255;
		
		for( like = i = 0; i < PALETTE_SIZE; i++, lp++ )
		{              
			if( *((COLORREF*)lp) == color )
				return i;	//最嘉匹配
			lpsrc = (const BYTE *)lp;
			lpdst = (const BYTE *)&color;
			//得到差值
			d1 = *lpdst++ - *lpsrc++; 
			d2 = *lpdst++ - *lpsrc++; 
			d3 = *lpdst++ - *lpsrc++;
			lDiff = d1 * d1 + d2 * d2 + d3 * d3;
			//选择误差较小的
			if( lDiff < lMinValue )
			{
				lMinValue = lDiff;
				like = i;
			}
		}
		return like;
	}
}

